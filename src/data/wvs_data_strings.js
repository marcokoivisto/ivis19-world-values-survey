export default function dimensionNames(key)  {
  switch (key) {
    case 'independence':
      return 'Independence'
      break;
    case 'hard_work':
      return 'Hard work'
      break;
    case 'saving_money':
      return 'Thrift saving money and things'
      break;
    case 'unselfishness':
      return 'Unselfishness'
      break;
    case 'imagination':
      return 'Imagination'
      break;
    case 'tolerance_and_respect':
      return 'Tolerance and respect for other people'
      break;
    case 'determination':
      return 'Determination, perseverance'
      break;
    case 'obedience':
      return 'Obedience'
      break;
  
    default:
      break;
  }
}